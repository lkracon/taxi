package com.taxi.pojo;



public class ErrorMessage {
	
	private String message;
	
	private int code;

	public ErrorMessage() {
	    
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}
}
